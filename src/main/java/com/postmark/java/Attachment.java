package com.postmark.java;

import com.google.gson.annotations.SerializedName;

/**
 * Attachment for a message.<p/>
 * Sample code:
 * <pre>
 * {@code
 * Attachment attachment = new Attachment();
 * attachment.setContentType("application/pdf");
 * attachment.setName("SomethingAwesome.pdf");
 *
 * // convert file contents to base64
 * File file = new File("SomethingAwesome.pdf");
 * byte[] ba = new byte[(int) file.length()];
 * FileInputStream fis = new FileInputStream(file);
 * Fileis.read(ba);
 * fis.close();
 * attachment.setContent(new Base64().encodeToString(ba));
 *
 * // can handle multiple attachments
 * Vector<Attachment> v = new Vector<Attachment>();
 * v.add(attachment);
 * message.setAttachments(v);
 * }
 * </pre>
 */
public class Attachment {
	@SerializedName("Name")
	private String name;
	@SerializedName("ContentType")
	private String contentType;
	@SerializedName("Content")
	private String content;
	@SerializedName("ContentID")
	private String contentID;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getContentType() {
		return contentType;
	}

	public void setContentType(String contentType) {
		this.contentType = contentType;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getContentID() {
		return contentID;
	}

	public void setContentID(String contentID) {
		this.contentID = contentID;
	}

	@Override
	public String toString() {
		return "Attachment [name=" + name + ", contentType=" + contentType + ", content=" + content + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((content == null) ? 0 : content.hashCode());
		result = prime * result + ((contentType == null) ? 0 : contentType.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Attachment other = (Attachment) obj;
		if (content == null) {
			if (other.content != null)
				return false;
		} else if (!content.equals(other.content))
			return false;
		if (contentType == null) {
			if (other.contentType != null)
				return false;
		} else if (!contentType.equals(other.contentType))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}
}